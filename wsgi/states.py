def get_state(city):
	city = city.lower()
	global states
	for state in states.keys():
		if city in states[state]:
			return state
		if city == state.lower():
			return state
		if city == 'all over india':
			return 'All over India'
		if city == 'n/a':
			return 'All over India'
		
